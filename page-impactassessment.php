        <div id="significance">
            <h2>社会的インパクト評価の意義</h2>
            <div class="container">
                <div class="text accent">社会的インパクト評価を実施する主たる意義は以下の二つです。</div>
                <div class="shadow_box">
                    <ol>
                        <li>
                            <div class="header_number"><div class="number">1</div>事業や活動の利害関係者に対する説明責任を果たすこと。（Prove）</div>
                            <div class="text accent">
                                外部の利害関係者に、社会的インパクトに係る戦略と結果を開示するという目的。<br>
                                団体が生み出した社会的価値が明確になるため、資源提供者とのコミュニケーションの円滑化や、社会的事業の有効性を PRすることが可能となる。
                            </div>
                        </li>
                        <li>
							<br>
                            <div class="header_number"><div class="number">2</div>事業や活動における学び・改善に活用すること。（Improve）</div>
                            <div class="text accent">
                                組織内部で社会的インパクトに係る戦略と結果を共有し、事業／組織に対する理解を高め、意思決定の判断材料を提供することで、事業運営や組織の在り方を改善するという目的。評価実施の過程で、事業の検証作業を実施するため、活動内容や目標を見直す機会が生じ、組織の成長につながる。
                            </div>
                        </li>
                    </ol>
                    <small>（G8社会的インパクト投資国内諮問委員会 社会的インパクト評価ワーキング・グループ(2016) 『社会的インパクト評価ツールセット 実践マニュアル』より）</small>
                </div>
                <div class="text">
                    こうした二つの目的を社会的インパクト評価によって達成することで、より成果の高い事業を継続的に実施することが可能になると考えられています。
                </div>
            </div>
