        <div id="roots">
            <h2>社会的インパクトとは</h2>
            <div class="container">
                <div class="shadow_box">
                    社会的インパクト評価とは、事業や活動の短期・長期の変化を含めた結果から生じた「社会的・ 環境的な変化、便益、学び、その他効果」を定量的・定性的に把握し、事業や活動について価値判断を加えることである。
                    <small>（G8社会的インパクト投資国内諮問委員会 社会的インパクト評価ワーキング・グループ(2016) 『社会的インパクト評価ツールセット 実践マニュル』より）</small>
                </div>
                <div class="text">
                    　社会的インパクト評価は、「事業を適切に評価することでその社会的成果を高め、社会課題の解決により寄与したい」「より成果の高い事業に資金を提供したい」と考える事業者や資金提供者などが増加したことにより、近年、急速に発展してきた考え方の一つです。<br>
                     <br>　これまで社会的事業は、「どのような対象者に対して、何人ぐらいに、どんなサービスを提供したか」といった”アウトプット（結果）”を成果報告とすることが一般的でした。しかし、「事業実施によって、どのような変化が社会に起きたのか」という"アウトカム（成果）"を重視する潮流が国際的に高まりつつあります。<br>
                     <br>　こうしたアウトカムを測り、事業改善につなげ、事業者や資金提供者のアカウンタビリティ（説明責任）を果たしていくために、社会的インパクト評価が注目を集めています。<br>
                    <br>　2014年にはG8（主要8カ国首脳会議）において、英国キャメロン首相の呼びかけにより、社会的インパクト投資をグローバルに推進することを目的として「G8社会的インパクト投資タスクフォース（The Social Impact Investment Taskforce）」が発足、日本でも国内諮問委員会が創設され、各界有識者が委員となり、各国の課題と取り組みの共有・協議を通して、インパクト投資における基本的なガイドラインを策定しました。（Social Value Japan 代表理事 伊藤健は、G8社会的インパクト投資国内諮問委員会 社会的インパクト評価ワーキング・グループに参加し、『社会的インパクト評価ツールセット』の作成に携わっています。）「G8社会的インパクト投資タスクフォース」は、2015年に民間組織であるGlobal Social Investment Steering Groupに改組され、現在も活発に活動を続けています。<br>
                    <br>　また、2016年に政府の経済財政諮問会議が「骨太方針2016」を発表し、その中でも「社会的成果（インパクト）評価」の促進が明記されたこと、「ニッポン一億総活躍プラン」、「まち・ひと・しごと創生基本方針2016」、「日本再興戦略2016」、「未来投資戦略」等においても社会的インパクト評価・投資が政府の基本方針として位置付けられたことなどから、国内においてもその取り組みが増加しています。
