                    <h4>PFS・SIBを用いた行政事業のスキーム</h4>
                    <div class="text">
                        成果連動型契約(PFS)・ソーシャルインパクトボンド(SIB)を用いたスキームでは、まず社会課題解決のための事業に対して、関心のある投資家が資金提供を行い、その資金を元に事業を実施します。<br>
                        事業を実施する事業者は、「革新的で実験的な新しいアイディアのある企業や非営利組織」を行政・中間支援組織が選定します。
                    </div>
                    <div class="sib">
                        <ul class="phase_list">
