                                    成功と判断した場合のみ、事前に合意した契約内容に基づき、事業実施コストに投資家のリターンを加味した金額を投資家に償還します。<br>
                                    この時、事前に合意した目標に到達しなかった場合、投資家への償還は発生せず、事業は終了となります。
