                                    １）成果連動型契約(PFS)・ソーシャルインパクトボンド(SIB)事業においては、革新的で実験的な事業を実施する事業者に対して、民間資金を導入しての事業実施を行います
                                </div>
                                <div class="image">
                                    <img src="<?=get_template_directory_uri()?>/img/aboutsib_phase_1_image.png" alt="">
                                </div>
                                <div class="text">
