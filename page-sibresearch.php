        <h4>概要</h4>
        <div class="text">
            <p>
                厚生労働省は平成29年度から令和元年度の３年間にわたって、PFSを活用した「保健福祉分野における民間活力を活用した社会的事業の開発・普及のための環境整備事業」を試行的に実施され、社会的事業の成果を測定する指標の設定、事業成果に基づく報酬の設定、行政や民間資金等の提供者との契約締結などの環境整備、行政による財政支援の成果指向型への改革等について、その課題や有効性の検証を行いました。
                <br>特定非営利活動法人ソーシャルバリュージャパンは、本事業のうち、大阪府池田市の事例に中間支援組織として参画いたしました。
