    <h2>成果連動型契約(PFS)／ソーシャル・インパクト・ボンド(SIB)の調査・研究</h2>
    <div class="container">

        <h3 class="header_label">PFS/SIBに関する研究会</h3>

        <h4>趣旨</h4>
        <div class="text">
            近年の日本社会において、社会的サービスの提供を、社会的投資や成果連動型契約(Pay for Success, PFS)による民間の事業ノウハウや資金を活用して効果的に実施し、社会イノベーションを促進しようという動きが加速しています。この手法は、特に2010年に英国で再犯防止分野において第1号案件が導入されたソーシャル・インパクト・ボンド(SIB)において普及が加速し、その導入は、2018年末時点では世界で132件、委託金額にして431百万ドル以上の実績があります。
            <br>日本においても、特に少子高齢化による社会的資源の逼迫から、限られた予算を活用して最大限の社会的成果の達成に対する要請があり、こうしたモデルの導入は、平成29年に神戸市、八王子市において、それぞれ糖尿病重症化予防、平成30年には広島県において同じく大腸がん検診の事業が実施される等、複数の自治体で実験的な取り組みが続いており、現在検討中の自治体を含めると全国で20以上の案件形成が進んでいるとされています。また、内閣府においても、令和元年度に成果連動型契約やソーシャル・インパクト・ボンドを推進する部署が設置され、経産省や省庁横断型での推進が行われています。
             <br>しかしながら、こうした成果連動型の公的資金の投入や民間資金の活用が、どのような条件下で社会課題の解決を誘導する結果になるのか、また導入の結果、どのようにその社会的成果を生み出すのかについては、こうした動きをけん引する欧米諸国でも様々な議論がある状況です。
             <br>本事業は、こうした事業に取り組む行政、事業者、資金提供者、中間支援組織等が議論の場を共有することで、異なる観点から成果連動型契約やソーシャル・インパクト・ボンドの取り組みについて検討を行い、こうした政策導入が行政と民間にどのようなインパクトをもたらすのかについての経験を共有し、日本と海外における実践についての知見をもたらすことで、社会課題解決に貢献することが期待するものです。
        </div>

        <h4>事業目的</h4>
        <div class="text">
            <ul>
                <li>日本におけるPFS/SIBの意義と、導入の課題についての論点整理を行い、行政・資金提供者・事業者・中間支援組織等の関係者間での認識の共有とネットワークの形成を行う</li>
                <li>日本においてPFS/SIBの推進を行うための検討が必要な課題を議論し、あるべき取り組みの方向性を位置づける</li>
                <li>次年度以降に日本での実施が想定されるPFS/SIBの取組をもとに、異なるレベルで取られるべきアクションを確認する</li>
            </ul>
        </div>


