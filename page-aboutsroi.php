            <h2>SROIの概要</h2>
            <div class="container">
                <div class="text">
                    事業への投資価値を、金銭的価値だけでなく、より広い価値の概念に基づき、評価や検証を行うためのフレームワークがSROI（Social Return on Investment：社会的投資収益率）です。この指標では、社会・環境・経済面の費用と便益とを以て様々な活動による社会的インパクトを評価し、その社会的価値を適切に評価することを目指しています。
                </div>
