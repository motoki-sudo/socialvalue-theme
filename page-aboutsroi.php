                    英国リバプールに拠点を置くFRCグループは、若年者や失業者に対して、中古家具のリサイクルや家具の製造・販売事業を通じた職業訓練・自立支援を行っています。若者や失業者が職業訓練として、回収された中古家具の解体や修理作業を行い、リサイクルされた中古家具をグループ会社のBulky Bob‘sを通じて販売しています。主な顧客は低所得層です。<br>
